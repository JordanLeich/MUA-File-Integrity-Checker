def calculate_md5(file_path):
    """Calculate the MD5 checksum of a file."""
    md5_hash = hashlib.md5()
    with open(file_path, 'rb') as file:
        for chunk in iter(lambda: file.read(4096), b''):
            md5_hash.update(chunk)
    return md5_hash.hexdigest()

def verify_integrity(directory_path, checksums):
    """Verify the integrity of game files in a directory."""
    global matched_counter, missing_counter, altered_counter, missing_files, altered_files
    try:
        for file_name, expected_checksum in checksums.items():
            file_path = os.path.join(directory_path, file_name.replace('/', os.sep))
            if not os.path.exists(file_path):
                print(f"File '{file_name}' is missing.")
                missing_counter += 1
                missing_files.append(file_name)
                continue
            actual_checksum = calculate_md5(file_path)
            if actual_checksum == expected_checksum:
                print(f"'{file_name}' matched successfully.")
                matched_counter += 1
            else:
                print(f"File '{file_name}' is either altered or corrupt.")
                altered_counter += 1
                altered_files.append(file_name)
    except Exception as e:
        print("An error occurred during file verification:")
        print(e)

